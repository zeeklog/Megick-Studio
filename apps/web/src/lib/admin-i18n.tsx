const messages = {
  en,
  "zh-CN": zh,
  "zh-TW": zhTWAdminMessages,
  ja: jaAdminMessages,
  fr: frAdminMessages,
  de: deAdminMessages,
} as const;

export type AdminTranslationKey = keyof typeof en;
export type AdminTranslationValues = Record<string, string | number | null | undefined>;
type AdminLocaleMessages = Partial<Record<AdminTranslationKey, string>>;

export const adminLocaleLabels: Record<AppLocale, string> = {
  ...localeLabels,
};

export function adminTranslate(
  locale: AppLocale,
  key: AdminTranslationKey,
  values?: AdminTranslationValues,
) {
  const localeMessages: AdminLocaleMessages = messages[locale];
  const fallbackMessages: AdminLocaleMessages = messages[FALLBACK_LOCALE];
  const template = localeMessages[key] ?? fallbackMessages[key] ?? key;
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_match: string, token: string) => {
    const value = values[token];
    return value == null ? "" : String(value);
  });
}

export function useAdminI18n() {
  const { locale, setLocale } = useI18n();

  return useMemo(() => {
    const intlLocale = localeToIntl(locale);
    return {
      locale,
      setLocale,
      supportedLocales,
      localeLabels: adminLocaleLabels,
      t: (key: AdminTranslationKey, values?: AdminTranslationValues) =>
        adminTranslate(locale, key, values),
      formatNumber: (value: number) => new Intl.NumberFormat(intlLocale).format(value),
      formatDate: (value: string | number | Date) => new Date(value).toLocaleDateString(intlLocale),
      formatDateTime: (value: string | number | Date) =>
        new Date(value).toLocaleString(intlLocale, {
          month: "short",
          day: "numeric",
          hour: "2-digit",
          minute: "2-digit",
        }),
    };
  }, [locale, setLocale]);
}
